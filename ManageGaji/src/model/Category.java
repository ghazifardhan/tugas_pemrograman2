package model;

/**
 *
 * @author markibid
 */
public class Category {
    
    private Integer id;
    private Integer typeId;
    private String name;

    public Category(Integer id, Integer typeId, String name) {
        this.id = id;
        this.typeId = typeId;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTypeId() {
        return typeId;
    }

    public void setTypeId(Integer typeId) {
        this.typeId = typeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
