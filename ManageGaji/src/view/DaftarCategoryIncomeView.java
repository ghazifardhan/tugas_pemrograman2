    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(id != 0){
            String name = tfName.getText();
            String set = "name = '" + name + "'";
            Database.updateData(tableName, set, id);
            generateData();
            tfName.setText("");
            id = 0;
        } else {
            JOptionPane.showMessageDialog(this, "Please select data before updating.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = tfName.getText();
        String attr = "(type_id, name)";
        String value = "('1', '"+name+"')";
        Database.insertData(tableName, attr, value);
        tfName.setText("");
        generateData();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(id != 0){
            Database.deleteData(tableName, id);
            generateData();
            tfName.setText("");
            id = 0;
        } else {
            JOptionPane.showMessageDialog(this, "Please select data before deleting.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();

        id = Integer.parseInt(model.getValueAt(row, 0).toString());
        String name = model.getValueAt(row, 1).toString();

        tfName.setText(name);
    }//GEN-LAST:event_btnSelectActionPerformed

    private void generateData(){
        model.setRowCount(0);
        ResultSet rs = Database.searchData(tableName, "type_id = 1");
        try{
            while(rs.next()){
               Object[] data = { rs.getString("id"), rs.getString("name")};
               model.addRow(data);
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }

