    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        asset_id = itemAccount.get(cbAccount.getSelectedIndex()).getId();
        category_id = itemCategory.get(cbCategory.getSelectedIndex()).getId();
        
        Integer amount = Integer.parseInt(tfAmount.getText());
        String date =  ((JTextField)dcDate.getDateEditor().getUiComponent()).getText();
        String desc = taDescription.getText();
        
        String attr = "(asset_id, category_id, amount, description, transaction_date, transaction_type)";
        String value = "('"+ asset_id +"','"+ category_id +"','"+ amount +"','"+ desc +"','"+ date +"','"+ trans_type +"')";
        Database.insertData("inoutcome", attr, value);
        taDescription.setText("");
        tfAmount.setText("");        
    }//GEN-LAST:event_btnSubmitActionPerformed

