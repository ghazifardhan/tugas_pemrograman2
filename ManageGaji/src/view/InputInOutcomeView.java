    private void generateAccountData(){
        ResultSet rs = Database.selectData("asset");
        try{
            while(rs.next()){
               itemAccount.add(new Account(rs.getInt("id"), rs.getString("name")));
               cbAccount.addItem(rs.getString("name"));
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }
    
    private void generateCategoryData(Integer type_id){
        String query = "type_id = " + type_id;
        ResultSet rs = Database.searchData("category", query);
        try{
            while(rs.next()){
               itemCategory.add(new Category(rs.getInt("id"), rs.getInt("type_id"), rs.getString("name")));
               cbCategory.addItem(rs.getString("name"));
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }

